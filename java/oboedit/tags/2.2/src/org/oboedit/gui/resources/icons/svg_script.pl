=head1 NAME

svg_script.pl - creates an SVG file from input parameters

=head1 SYNOPSIS

svg_script.pl -letter c -letter_colour FF00FF -shape square -shape_colour 000000 -output my_svg_file.svg

=head1 DESCRIPTION

Creates an icon in SVG format from a set of parameters.

=head2 Arguments

-letter, -l    : the letter or glyph

-letter_colour : colour of the letter, as a six character html code,
                 defaults to white if not set

-shape, -s     : the shape of the background image
                 defaults to square if not set

-shape_colour  : colour of the background image, html code
                 defaults to black if not set

-output        : the file to save the image to
                 prints to STDOUT if a file name is not given

The resulting SVG is 60x60px, although the area covered by the shape (and letter)
will be smaller. This is to ensure that the letter is in the same place regardless of the background shape.

Please note that it is possible that the letters may not fit into the shape
completely.

=head2 Letters and Glyphs

The following glyphs and letters are available:

a - z, A - Z, and 0 - 9;

Various other characters:

Glyph	use -letter XXX

!		1_UC

@		2_UC

		3_UC

$		4_UC

%		5_UC

^		6_UC

&		7_UC

*		8_UC

(		9_UC

)		0_UC

-		minus

+		plus

=over

=item Greek alphabet

All letters of the standard Greek alphabet are available. To specify a lowercase
letter, use greek_[letter name]; for an uppercase letter, use greek_capital_[letter name] or greek_[letter name with first letter capitalized].

e.g.

lowercase alpha: -letter greek_alpha

uppercase gamma: -letter greek_capital_gamma  or  -letter greek_Gamma

The letters of the Greek alphabet are:

alpha, beta, gamma, delta, epsilon, zeta, eta, theta, iota, kappa, lamda, mu, nu, xi, omicron, pi, rho, sigma, tau, upsilon, phi, chi, psi, omega


