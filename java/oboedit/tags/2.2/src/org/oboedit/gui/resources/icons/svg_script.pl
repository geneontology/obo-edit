#!/usr/bin/perl -w

use strict;
use warnings;

use Data::Dumper;
use Getopt::Long;

if (!@ARGV) {
	system("perldoc $0");
	exit;
}
if ("@ARGV" eq "-h") {
	system("perldoc $0");
	exit;
}

my $opt = {};
GetOptions($opt,
	"help|h|?",
	"verbose|v",
	"output|o=s",
	"letter|l=s",
	"shape=s",
	"letter_colour|l_colour|letter_color|l_color=s",
	"shape_colour|s_colour|shape_color|s_color=s",
	);

if ($opt->{help}) {
	system("perldoc $0");
	exit;
}

my $verbose = $opt->{verbose};

my $defaults = {
	shape => 'square',
	shape_colour => '000000',
	letter_colour => 'FFFFFF',
};

my $shape_h = {
	rectangle => { tag => 'rect', coords => 'x="3.5" y="11.5" width="52" height="36"' },
	circle => { tag => 'circle', coords => 'cx="29.5" cy="29.5" r="25.6"' },
	diamond => { tag => 'rect', coords => 'x="8.3" y="8.301" transform="matrix(0.7071 -0.7071 0.7071 0.7071 -12.2193 29.4997)" width="42.399" height="42.398"'},
	square => { tag => 'rect', coords => 'x="7.5" y="7.5" width="44" height="44"'},
	r_arrow => { tag => 'polygon', coords => 'points="60,30 32,0 32,8 8,8 8,52 32,52 32,60"'},
	l_arrow => { tag => 'polygon', coords => 'points="8,30 36,60 36,52 60,52 60,8 36,8 36,0"'},
	up_arrow => { tag => 'polygon', coords => 'points="30,0 0,28 8,28 8,52 52,52 52,28 60,28"'},
	down_arrow => { tag => 'polygon', coords => 'points="52,32 52,8 8,8 8,32 0,32 30,60 60,32"'},
};

my @errs;
my $svg_data;

## check we have the letter
if (! defined $opt->{letter})
{	push @errs, "A letter must be specified.";
}
else # $opt->{letter} is defined
{	$svg_data->{letter} = svg_data($opt->{letter});
	if (! $svg_data->{letter} )
	{	push @errs, "The letter ".$opt->{letter}." could not be found.";
	}
}


## check the shape
if (! defined $opt->{shape})
{	# use the default shape
	$svg_data->{shape} = { id => $defaults->{shape}, %{$shape_h->{ $defaults->{shape}}} };
	print STDERR "Using default shape\n" if $verbose;
}
elsif (! $shape_h->{ lc $opt->{shape} })
{	push @errs, "The background shape ".$opt->{shape}." could not be found.";
}
else
{	$svg_data->{shape} = { id => lc $opt->{shape}, %{$shape_h->{ lc $opt->{shape} }}};
}

## we need all the options to be specified.
foreach my $o qw( letter shape )
{	if ($opt->{ $o."_colour" })
	{	# make sure that it is the right format
		# remove any non-digit chars
		$opt->{ $o."_colour" } =~ s/[^a-f\d]//ig;
		if ($opt->{ $o."_colour" } =~ /^([a-f\d])([a-f\d])([a-f\d])$/i)
		{	# double each number in the code
			$opt->{ $o."_colour" } = "$1$1$2$2$3$3";
		}
		elsif ($opt->{ $o."_colour" } =~ /[a-f\d]{6}/i)
		{	# chop off any extra digits
			$opt->{ $o."_colour" } =~ s/^([a-f\d]{6}).*/$1/i;
		}
		else
		{	push @errs, $o."_colour must be specified as a six digit html colour code";
			delete $opt->{ $o."_colour" };
		}
	}
	else
	{	$opt->{ $o."_colour" } = $defaults->{ $o."_colour" };
		print STDERR "Using the default $o colour\n";
	}
	
	# if the colour is OK and we have svg data for the shape, add the colour data
	if ($opt->{ $o."_colour" } && $svg_data->{$o})
	{	$svg_data->{$o}{colour} = $opt->{ $o."_colour" };
	}
}

# check that the shape and letter colours are different...
if ($opt->{letter_colour} && $opt->{shape_colour} && $opt->{letter_colour} eq $opt->{shape_colour})
{	push @errs, "The letter and background colours must be different!";
}

## check that the file name specified is OK
if (! defined $opt->{output})
{	## print to STDOUT instead
	print STDERR "No output specified; printing to STDOUT\n" if $verbose;
	$opt->{out} = \*STDOUT;
}
elsif ( -f $opt->{output} ) # if it already exists...
{	push @errs, "File ". $opt->{output}." already exists";
}
else
{	if (open FH, '>', $opt->{output})
	{	$opt->{out} = \*FH;
	}
	else
	{	push @errs, "Can't open ".$opt->{output}.": $!";
	}
}

if (! @errs)
{	# try opening the file...
	write_svg( $opt->{out}, $svg_data );
	close $opt->{out} if $opt->{output};
}
else
{	unshift @errs, "The following errors were found when trying to create an SVG:";
	die join("\n", @errs);
}

exit(0);


sub write_svg {
	my $out = shift;
	my $svg_data = shift;

	print $out
'<?xml version="1.0" encoding="UTF-8" standalone="no"?>
<svg
   xmlns:svg="http://www.w3.org/2000/svg"
   xmlns="http://www.w3.org/2000/svg"
   version="1.0" width="60" height="60" viewBox="0 0 60 60"
   id="custom_svg"
   xml:space="preserve">
';

# print out the bg picture...
	print $out '
	<' . $svg_data->{shape}{tag} 
		. ' id="' . $svg_data->{shape}{id}
		. '" ' . $svg_data->{shape}{coords}
		. ' style="fill:#' . $svg_data->{shape}{colour} . '" />';

# print out the letter
	print $out '
	<path id="' . $svg_data->{letter}{id}
		. '" d="' . $svg_data->{letter}{d}
		. '" style="fill:#' . $svg_data->{letter}{colour} . '" />';

	print $out '
</svg>
';
}


